import React, { PureComponent, Component } from 'react';
import axios from 'axios';
import './staff.css';
import jsPDF from 'jspdf';
import 'jspdf-autotable'


const staff = props => (
      <tr>
    <td>{props.staff._id}</td>
    <td>{props.staff.name}</td>
    <td>{props.staff.department}</td>
    <td>{props.staff.nic}</td>
    <td>{props.staff.gender}</td>
    <td>{props.staff.phone}</td>
    <td>{props.staff.email}</td>

    <td>
      
    </td>
</tr>
)

export default class pdfGenerator extends PureComponent {
    constructor(props) {
        super(props);

        this.state = { staff: [] };
    }

    componentDidMount() {
        axios.get('http://localhost:5000/staff/')
            .then(response => {
                this.setState({ staff: response.data })
            })
            .catch((error) => {
                console.log(error);
            })
    }

    

    jsPdfGenerator = () => {
        var doc = new jsPDF('p', 'pt');

        doc.setFont("calibri")
        doc.setFontSize(22)
        doc.setFont(undefined, 'bold')
        doc.text(175, 30, "Staff  Report", 'center')
        doc.text(175, 30, "  ",)

        doc.autoTable({ html: '#salaryDetailsTable' })

        doc.save("Staff_Report.pdf");


    }
    staffMembersList() {
        return this.state.staff.map(currentstaff => {
            return <staff staff={currentstaff} deleteStaff={this.deleteStaff} key={currentstaff._id} />;
        })
    }

































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































    

    render() {
        return (
            <div className="salaryReportPage">
                <br />
                <div className='container'>
                    <h3 className="staffReportTitle">STAFF REPORT</h3>
                    <br />
                    <table className="table" id="salaryDetailsTable">
                        <thead className="thead-light">
                        <tr>
                                <th>Staff ID</th>
                                <th>Name</th>
                                <th>Department</th>
                                <th>NIC</th>
                                <th>Gender</th>
                                <th>Phone</th>
                                <th>Email</th>
                                <th>Action</th>
                            </tr>
                        </thead>
                        <tbody>
                            {this.staffMembersList()}
                        </tbody>
                    </table>

                    <button onClick={this.jsPdfGenerator} className="generateReportBtn">GENERATE REPORT</button>
                </div>
            </div>
        )
    }
}